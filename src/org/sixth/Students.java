package org.sixth;

public class Students {

	private int id;
	
	private String name;
	
	private float fee;

	public int getId() {
		return id;
	}
	public void setId(int x) {
		this.id = x;
	}

	public String getName() {
		return name;
	}

	public void setName(String y) {
		this.name = y;
	}

	public float getFee() {
		return fee;
	}

	public void setFee(float z) {
		this.fee = z;
	}
	
	
}
